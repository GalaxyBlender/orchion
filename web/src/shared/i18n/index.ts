void i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    detection: {
      caches: [],
      lookupLocalStorage: languageOverrideKey,
      order: ["localStorage", "navigator"],
    },
    fallbackLng: "en",
    interpolation: { escapeValue: false },
    resources,
    supportedLngs: supportedLanguages,
  });

export function normalizeLanguage(language: string | undefined): SupportedLanguage {
  if (!language) return "en";
  const lower = language.toLowerCase();
  if (lower.startsWith("zh-tw") || lower.startsWith("zh-hk") || lower.startsWith("zh-hant")) {
    return "zh-TW";
  }
  if (lower.startsWith("zh")) {
    return "zh-CN";
  }
  return "en";
}

export function detectBrowserLanguage(): SupportedLanguage {
  if (typeof window === "undefined") {
    return "en";
  }

  return normalizeLanguage(window.navigator.languages?.[0] ?? window.navigator.language);
}

export function currentLanguageSelection(): LanguageSelection {
  const storedLanguage = readStoredLanguageOverride();
  return storedLanguage ?? "auto";
}

export function readStoredLanguageOverride(): SupportedLanguage | undefined {
  if (typeof window === "undefined") {
    return undefined;
  }

  try {
    const storedLanguage = window.localStorage.getItem(languageOverrideKey);
    return isSupportedLanguage(storedLanguage) ? storedLanguage : undefined;
  } catch {
    return undefined;
  }
}

export function setLanguageSelection(selection: LanguageSelection): void {
  if (typeof window !== "undefined") {
    try {
      if (selection === "auto") {
        window.localStorage.removeItem(languageOverrideKey);
      } else {
        window.localStorage.setItem(languageOverrideKey, selection);
      }
    } catch {
    }
  }

  void syncLanguageFromSelection(selection);
}

export async function syncLanguageFromSelection(selection: LanguageSelection): Promise<void> {
  const nextLanguage = selection === "auto" ? detectBrowserLanguage() : selection;
  if (i18n.resolvedLanguage !== nextLanguage && i18n.language !== nextLanguage) {
    await i18n.changeLanguage(nextLanguage);
  }
  if (typeof document !== "undefined") {
    document.documentElement.lang = nextLanguage;
  }
}

function isSupportedLanguage(language: unknown): language is SupportedLanguage {
  return language === "en" || language === "zh-CN" || language === "zh-TW";
}

void syncLanguageFromSelection(currentLanguageSelection());

export default i18n;
